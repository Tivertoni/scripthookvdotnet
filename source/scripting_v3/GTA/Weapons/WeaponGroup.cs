        /// <summary>
        /// Melee weapons such as knives, bats, and similar.
        /// </summary>
        Melee = 3566412244u,

        /// <summary>
        /// Pistols and handguns.
        /// </summary>
        Pistol = 416676503u,

        /// <summary>
        /// Submachine guns (SMGs).
        /// </summary>
        SMG = 3337201093u,

        /// <summary>
        /// Assault rifles.
        /// </summary>
        AssaultRifle = 970310034u,

        /// <summary>
        /// Metal detectors and scanning devices.
        /// </summary>
        DigiScanner = 3539449195u,

        /// <summary>
        /// Fire extinguishers.
        /// </summary>
        FireExtinguisher = 4257178988u,

        /// <summary>
        /// Machine guns.
        /// </summary>
        MG = 1159398588u,

        /// <summary>
        /// Night vision devices.
        /// </summary>
        NightVision = 3493187224u,

        /// <summary>
        /// Parachutes.
        /// </summary>
        Parachute = 431593103u,

        /// <summary>
        /// Shotguns.
        /// </summary>
        Shotgun = 860033945u,

        /// <summary>
        /// Sniper rifles.
