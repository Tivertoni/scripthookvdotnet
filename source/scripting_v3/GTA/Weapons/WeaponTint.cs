        /// <summary>
        /// Green tint
        /// </summary>
        Green,

        /// <summary>
        /// Gold tint
        /// </summary>
        Gold,

        /// <summary>
        /// Pink tint
        /// </summary>
        Pink,

        /// <summary>
        /// Army tint
        /// </summary>
        Army,

        /// <summary>
        /// LSPD tint
        /// </summary>
        LSPD,

        /// <summary>
        /// Orange tint
        /// </summary>
        Orange,

        /// <summary>
        /// Platinum tint
        /// </summary>
        Platinum,
    }
}
